#!/bin/bash

# =================================================================
# 启动前后端服务
# =================================================================

echo "=========================================="
echo "启动前后端服务"
echo "=========================================="
echo ""

# 启动后端
echo ">>> [1/2] 启动后端服务..."
bash scripts/start.sh

if [ $? -ne 0 ]; then
    echo "❌ 后端启动失败，请检查错误信息"
    exit 1
fi

echo ""
echo "等待 3 秒..."
sleep 3

# 启动前端
echo ""
echo ">>> [2/2] 启动前端服务..."
bash scripts/start_frontend.sh

if [ $? -ne 0 ]; then
    echo "⚠️  前端启动失败，但后端已运行"
    echo "   可以稍后单独启动前端: bash scripts/start_frontend.sh"
fi

echo ""
echo "=========================================="
echo "启动完成"
echo "=========================================="
echo ""
echo "检查服务状态: bash scripts/check_all.sh"
echo ""
